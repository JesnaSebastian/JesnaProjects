package Day1;

public class Greaternum {
    public static void main(String[] args) {

        int a = 10, b = 78, c = 8;

        if (a > b && a > c) {
            System.out.println("a is the greater number " + a);
        } else if (b > c) {
            System.out.println("b is the greater number " + b);
        } else {
            System.out.println("c is the greater number " + c);
        }

    }

}
